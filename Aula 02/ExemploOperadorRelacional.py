# Apenas teste de operdores relacionais

a = 5
b = 10

print(a == b) #Verifica se dois valores são iguais
print(a != b) #Verifica se dois valores são diferentes
print(a > b) #Verifica se o valor à esquerda é maior que o valor à direita
print(a >= b) #Verifica se o valor à esquerda é maior ou igual ao valor à direita
print(a < b) #Verifica se o valor à esquerda é menor que o valor à direita
print(a <= b) #Verifica se o valor à esquerda é menor ou igual ao valor à direita





